%-----------------------------*转换矩阵*----------------------------------
%-----------------------------*静力学*----------------------------------
function TK = Transformation(DYJD,JDZB,L)
JDZB_1 = JDZB(DYJD(1,1),:);
JDZB_2 = JDZB(DYJD(2,1),:);
v = JDZB_2 - JDZB_1;
if size(JDZB,2) == 2 %判断是二维问题还是三维问题
    TK = [v(1)/L v(2)/L  0 0 ;
          0 0 v(1)/L v(2)/L];
else
    TK = [v(1)/L v(2)/L v(3)/L 0 0 0 ;
          0 0 0 v(1)/L v(2)/L v(3)/L];
end